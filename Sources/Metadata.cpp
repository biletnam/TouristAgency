	AddColumn("ФИО");

	AddTable("Страны");
	AddColumn("Название");

	AddTable("Туры");
	AddColumn("Назначение");
	AddKey("Страна", "Название", "Страны", "Код");

	AddTable("Путёвки");
	AddKey("Тур", "Назначение", "Туры", "Код");
	AddKey("Клиент", "ФИО", "Клиенты", "Код");
	AddColumn("Цена");

	// Добавление отчетов

	AddReport("Статистика");
	AddColumn("Наименование");
	AddColumn("Количество");

	AddReport("Продажи");
	AddColumn("Дата");
	AddColumn("Продажи");
